// Método que dibuja la factura en el Área de Texto
    private void generarTextoFactura() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm");
        
        double importeTotal = 0;
        for (DetalleFactura df : detalles) {
            importeTotal += df.getImporte();
        }
        
        double opGravadas = importeTotal / (1 + TASA_IGV);
        double igvCalculado = importeTotal - opGravadas;

        StringBuilder sb = new StringBuilder();
        sb.append("========================================================================================\n");
        sb.append(String.format("%55s\n", RAZON_SOCIAL));
        sb.append(String.format("%67s\n", DIRECCION_EMPRESA));
        sb.append("========================================================================================\n");
        sb.append("R.U.C. ").append(RUC_EMPRESA).append("\n");
        sb.append("FACTURA ELECTRÓNICA - NÚMERO: ").append(NRO_COMPROBANTE).append("\n");
        sb.append("----------------------------------------------------------------------------------------\n");
        sb.append("CLIENTE   : ").append(txtNombreCliente.getText()).append("\n");
        sb.append("R.U.C.    : ").append(txtRucCliente.getText()).append("\n");
        sb.append("DIRECCIÓN : ").append(txtDireccionCliente.getText()).append("\n");
        sb.append("FECHA     : ").append(sdf.format(new Date())).append("\n");
        sb.append("----------------------------------------------------------------------------------------\n");
        sb.append(String.format("%-8s %-10s %-25s %-15s %-12s %-12s\n", "CANT.", "UND.", "DESCRIPCIÓN", "DESPACHO", "P. UNITARIO", "IMPORTE"));
        sb.append("----------------------------------------------------------------------------------------\n");

        for (DetalleFactura detalle : detalles) {
            sb.append(detalle.obtenerFilaFormateada());
        }

        sb.append("----------------------------------------------------------------------------------------\n");
        sb.append(String.format("%65s S/ %-9.2f\n", "Op. Gravadas:", opGravadas));
        sb.append(String.format("%65s S/ %-9.2f\n", "IGV (18%):", igvCalculado));
        sb.append(String.format("%65s S/ %-9.2f\n", "Importe Total:", importeTotal));
        sb.append("========================================================================================\n");
        sb.append("Representación impresa de la Factura Electrónica de Petroperú.\n");
        sb.append("========================================================================================\n");
        
        txtAreaFactura.setText(sb.toString()); 
    }
