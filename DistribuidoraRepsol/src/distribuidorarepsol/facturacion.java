class DetalleFactura {
    private final double cantidad;
    private final String unidad;
    private final String descripcion;
    private final double precioUnitario;
    private final String tipoDespacho;

    public DetalleFactura(double cantidad, String unidad, String descripcion, double precioUnitario, String tipoDespacho) {
        this.cantidad = cantidad;
        this.unidad = unidad;
        this.descripcion = descripcion;
        this.precioUnitario = precioUnitario;
        this.tipoDespacho = tipoDespacho;
    }

    public double getImporte() { return cantidad * precioUnitario; }

    public String obtenerFilaFormateada() {
        return String.format("%-8.2f %-10s %-25s %-15s S/ %-9.2f S/ %-9.2f\n", 
                cantidad, unidad, descripcion, tipoDespacho, precioUnitario, getImporte());
    }
}
