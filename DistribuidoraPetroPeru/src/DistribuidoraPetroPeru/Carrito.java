    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
       
        int fila = tablaCarrito.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila para quitar.");
            return;
        }
        listaCarrito.remove(fila);
        cargarTabla();
        
   //----------->  se agrego BOTON QUITAR    <--------     
        
   
        
    }//GEN-LAST:event_btnQuitarActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        
        if (listaCarrito.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El carrito está vacío.");
            return;
        }
        javax.swing.JOptionPane.showMessageDialog(this, "Compra confirmada. Productos: " + listaCarrito.size());
        listaCarrito.clear();
        cargarTabla();
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

