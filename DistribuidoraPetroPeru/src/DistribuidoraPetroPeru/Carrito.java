    private void cargarTabla() {
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tablaCarrito.getModel();
        modelo.setRowCount(0);
        for (Producto p : listaCarrito) {
            modelo.addRow(new Object[]{
                p.getTipoCombustible(),
                p.getCantidadGalones(),
                String.format("S/ %.2f", p.getPrecioUnitario()),
                String.format("S/ %.2f", p.getSubtotal())
            });
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    

